package com.gaoling.admin.shop.entity;

public class EmployeeInfo {

	private int id;
	private String employeeId;
	private int sid;
	private String openId;
	private String unionName;
	private String unionPwd;
	private String unionState;
	private String createTime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getOpenId() {
		return openId;
	}

	public void setOpenId(String openId) {
		this.openId = openId;
	}

	public String getUnionName() {
		return unionName;
	}

	public void setUnionName(String unionName) {
		this.unionName = unionName;
	}

	public String getUnionPwd() {
		return unionPwd;
	}

	public void setUnionPwd(String unionPwd) {
		this.unionPwd = unionPwd;
	}

	public String getUnionState() {
		return unionState;
	}

	public void setUnionState(String unionState) {
		this.unionState = unionState;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

}
